package Starpractice;

public class Pattern3 { 
	
	public static void main(String[]args) {
		
		int star = 1 ;
		
		for (int i=5 ;i > 0; i--) {
			
			for (int j =5 ;j>=star;j--) {
				
				System.out.print("*");
				
			}
			System.out.println();
			star++;
		}
	
			
			
			
				
		}
	}


