package JAVABasics;

public class ArithmaticOperator2 {
       public static void main (String[]args) {
    	   
    	   //variable Declaration and Initialization
    	   
    	   int a=40 ,  b = 80;
    	   
    	   //addition operator
    	   
    	    System.out.println("a+b=" +(a+b ));
    	    
    	    //subtraction operator
    	    
    	    System.out.println("a-b=" +(a-b ));

    	    //multiplication operator 
    	    
    	    System.out.println("a*b=" +(a*b ));
    	    
    	    //division operator
    	    
    	    System.out.println("a/b=" +(a/b ));
    	    
    	    //module operator
    	    
    	    System.out.println("a%b=" +(a%b ));
    	   
    			   
    	   
    	   
    	   
    	   
       }
}
