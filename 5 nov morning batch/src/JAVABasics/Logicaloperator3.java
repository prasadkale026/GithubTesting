package JAVABasics;

public class Logicaloperator3 {
	
	public static void main (String[]args) {
		
		//  && operator AND 
		
		System.out.println((70<60)  && (40==40)) ;
		
		System.out.println((40==40) && (60>50)) ;
		
		// or operator 
		
		System.out.println((55==55)  &&  (88>46)) ;
		
    	System.out.println((46<36) && (40<33)) ;
    	
    	//  ! not operator 
    	
    	System.out.println( !(78>88)) ;
    	
    	System.out.println(! (55==55)) ;
	}

}
