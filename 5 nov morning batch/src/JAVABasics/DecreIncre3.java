package JAVABasics;

public class DecreIncre3 {

	public static void main(String[]args ) {
		
    short a= 150;
    
    System.out.println(a);
    System.out.println(a++);
    System.out.println(++a);
    
    //a=152
    
    System.out.println(a--);
    System.out.println(a);
    System.out.println(--a);
	}
	
}
