package JAVABasics;

public class Arithmaticoperator3  {
	
	public static void main (String[]args) {
		
		//variable declaration and initialization
		
		byte a=120 ,b=60 ;
		
		//addition operator
		
		System.out.println("a+b = " + (a+b));
		
		//subtraction operator
		
		System.out.println("a-b = " + (a-b)); 
		
		//multiplication operator 
		
		System.out.println("a*b = " + (a*b));
		
		//division operator 
		
		System.out.println("a/b = " + (a/b));
		
		//module operator
		
		System.out.println("a%b = " + (a%b));
		
	}

}
