package JAVABasics;

public class Logicaloperator2 {
	
	public static void main (String[]args) {
		
		//  && operator AND 
		
		System.out.println((80==80)&& (70>50));
		
		System.out.println((50>60) && (70==70)) ;
		
		// or operator ||
		
		System.out.println((60==70) || (70==70)) ;
		
		System.out.println((100>75)  ||  (40==40))  ;
		
		//  ! not operator
		
		System.out.println( !(55==55)) ;
		
		System.out.println( !(150==50)) ;
		
		
	}

}
