package JAVABasics;

public class DecreIncre5 {
	
	public static void main (String[]args) {
		
		short a=200;
		
		System.out.println(a);
		System.out.println(a++);
		System.out.println(++a);
		
		// a=202
		
		System.out.println(a--);
		System.out.println(a);
		System.out.println(--a);
	}

}
