package JAVABasics ;

public class Logicaloperator1 {
	
	public static void main (String[]args) {
		
		//  And operator
		
		System.out.println ((10>5) && (50==10)) ;
		
		System.out.println ((55==55) && (50>11)) ;
		System.out.println ((56==56) && (50>12)) ;
		
		// Or operator
		
		System.out.println ((10>5) ||   (50==50)) ;
		
		System.out.println ((10<5) || (50==10)) ;
		
		// not operator
		
		System.out.println (! (10==11) )  ;
		
		System.out.println (!(15==15) ) ;
		
		
	}

}
