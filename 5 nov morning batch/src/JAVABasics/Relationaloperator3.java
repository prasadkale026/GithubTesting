package JAVABasics;

public class Relationaloperator3 {
	
	public static void main (String[]args)  {
		
		short a=170 , b=150 ;
		
		System.out.println(a>b);
		
		System.out.println(a<b);
		
		System.out.println(a>=b);
		
		System.out.println(a<=b);
		
		System.out.println(a==b);
		
		System.out.println(a!=b);
	}
	
}
