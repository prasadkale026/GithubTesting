package JAVABasics;

public class Relationaloperator2 {
	
	public static void main (String[]args) {
		
		byte a=40 , b=35 ;
		
		System.out.println(a>b);  //less than
		
		System.out.println(a<b);  //greater than
		
		System.out.println(a<=b);  //less than equals to
		
		System.out.println(a>=b);  //greater than equals to
		
		System.out.println(a==b);  //equals to 
		
		System.out.println(a!=b);   //not equals to
	}

}
