package JAVABasics;

public class Arithmaticoperator1 {
	
	public static void main (String[]args ){
		
		// variable declaration and initialization
		
		int a=70 ,b=35; 
		
		//addition operator 
		
		System.out.println("a+b="+(a+b));
		
		//substaction operator
		
		System.out.println("a-b = " + (a-b));
		
		//muitiplication operator
		
		System.out.println("a*b = " + (a*b));
		
		//division operator 
		
		System.out.println("a/b = " + (a/b));
		
		//module operator
		
		System.out.println("a%b = " + (a%b));
	}
	
	

}
