package JAVABasics;

public class Relationaloperator4 {
	
	public static void main (String[]args) {
		
		byte a=100 ,b =120 ;
		
		System.out.println(a>b);
		
		System.out.println(a<b);
		
		System.out.println(a>=b);
		
		System.out.println(a<=b);
		
		System.out.println(a==b);
		
		System.out.println(a!=b);
	}

}
